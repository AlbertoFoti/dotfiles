#!/bin/bash

# Define the color variables
set primary_bg "#FF0000"
set color1 "#FF0000"
set color2 "#00FF00"
set color3 "#0000FF"
set primary_fg "#FFFFFF"

# Export the variables
export PRIMARY_BG=$primary_bg
export COLOR1=$color1
export COLOR2=$color2
export COLOR3=$color3
export PRIMARY_FG=$primary_fg

echo "Color palette variables exported."